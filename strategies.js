// Функция определения доминирующего типа
function getDominantType(scores) {
    let maxScore = -1;
    let dominantType = 'human';
    for (const [type, score] of Object.entries(scores)) {
        if (score > maxScore) {
            maxScore = score;
            dominantType = type;
        }
    }
    return STRATEGIES[dominantType] || STRATEGIES.default;
}
